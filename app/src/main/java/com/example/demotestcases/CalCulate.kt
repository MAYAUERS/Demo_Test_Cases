package com.example.demotestcases

class CalCulate {

    fun addTwoNos(a:Int,b:Int) :Int{
        var add=a+b
        return add
    }

    fun SubscracteTwoNos(a:Int,b:Int) :Int{
        var sub=a-b
        return sub
    }


    fun multiplyTwoNos(a:Int,b:Int):Int{
        return a*b
    }


    fun DivisionOfTwoNos(a:Int,b:Int):Int{
        return a/b
    }

}